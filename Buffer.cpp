//-----------------------------------------------------------------------------
// Construction/Destruction
//-----------------------------------------------------------------------------
CBuffer::CBuffer()
{
    m_size = 0;
    m_data = 0;
}

CBuffer::CBuffer( const char* data, size_t size )
{
    m_size = size;
    m_data = new char[m_size];
    memcpy( m_data, data, m_size );
}


CBuffer::~CBuffer()
{
    delete[] m_data;
}